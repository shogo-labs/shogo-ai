/**
 * App Scaffolding: Create complete app from Enhanced JSON Schema
 *
 * Copies base template, generates domain and routes, customizes for schema.
 */

import { cpSync, existsSync, mkdirSync, readFileSync, writeFileSync } from "fs"
import { execSync } from "child_process"
import { join, resolve, dirname } from "path"
import nunjucks from "nunjucks"
import type { EnhancedJsonSchema } from "../schematic/types"
import { createRoutes } from "./routes"

/**
 * App features configuration
 */
export interface AppFeatures {
  /** Include API routes (default: true) */
  api?: boolean
  /** Include basic styling (default: true) */
  styling?: boolean
}

/**
 * Configuration for app scaffolding
 */
export interface ScaffoldAppConfig {
  /** Project name (used for directory and package.json) */
  name: string
  /** Enhanced JSON Schema or path to schema file */
  schema: EnhancedJsonSchema | string
  /** Output directory (default: workspaces/{name}) */
  output?: string
  /** App features to include */
  features?: AppFeatures
  /** Skip dependency installation (default: false) */
  skipInstall?: boolean
  /** Dry run - return files without writing (default: false) */
  dryRun?: boolean
}

/**
 * Result of app scaffolding
 */
export interface ScaffoldAppResult {
  /** Absolute path to project directory */
  projectDir: string
  /** List of created/modified files (relative to projectDir) */
  files: string[]
  /** File contents if dryRun is true */
  fileContents?: Record<string, string>
}

