/**
 * Route Generator: Create Hono CRUD routes from Enhanced JSON Schema
 *
 * Generates standard REST endpoints for each entity in a schema.
 */

import nunjucks from "nunjucks"
import type { EnhancedJsonSchema } from "../schematic/types"

/**
 * Configuration for route generation
 */
export interface CreateRoutesConfig {
  /** Enhanced JSON Schema with entity definitions */
  schema: EnhancedJsonSchema
  /** Which entities to generate routes for (default: all) */
  entities?: string[]
  /** Base path prefix (default: '/api') */
  basePath?: string
  /** Include authentication middleware placeholder (default: true) */
  withAuth?: boolean
}

/**
 * Result of route generation
 */
export interface CreateRoutesResult {
  /** Generated TypeScript code */
  code: string
  /** Entities that routes were generated for */
  entities: string[]
}

/**
 * Entity info extracted from schema for template
 */
interface EntityInfo {
  name: string
  pluralName: string
  collectionName: string
  routePath: string
  properties: PropertyInfo[]
  hasReferences: boolean
}

interface PropertyInfo {
  name: string
  type: string
  required: boolean
  isReference: boolean
  referenceTarget?: string
}

// Nunjucks template for route generation
const ROUTES_TEMPLATE = `/**
 * Auto-generated CRUD routes for {{ schemaName }}
 *
 * Generated by @shogo/state-api generators
 * DO NOT EDIT - regenerate using createRoutes()
 */

import { Hono } from "hono"

/**
 * Domain store interface expected by routes
 */
export interface {{ schemaName | title | replace(" ", "") }}RoutesConfig {
  store: {
{%- for entity in entities %}
    {{ entity.collectionName }}: {
      query: () => {
        where: (filter: Record<string, any>) => {
          first: () => Promise<any>
          toArray: () => Promise<any[]>
        }
        toArray: () => Promise<any[]>
      }
      get: (id: string) => any
      insertOne: (data: any) => Promise<any>
      updateOne: (id: string, changes: any) => Promise<void>
      deleteOne: (id: string) => Promise<void>
    }
{%- endfor %}
  }
}

/**
 * Create all routes for {{ schemaName }}
 */
export function create{{ schemaName | title | replace(" ", "") }}Routes(config: {{ schemaName | title | replace(" ", "") }}RoutesConfig) {
  const { store } = config
  const router = new Hono()

{% for entity in entities %}
  // ===== {{ entity.name }} Routes =====
  const {{ entity.pluralName | lower }} = new Hono()

  // GET {{ basePath }}/{{ entity.routePath }} - List all
  {{ entity.pluralName | lower }}.get("/", async (c) => {
    try {
      const items = await store.{{ entity.collectionName }}.query().toArray()
      return c.json(items)
    } catch (error: any) {
      return c.json({ error: { code: "query_error", message: error.message } }, 500)
    }
  })

  // GET {{ basePath }}/{{ entity.routePath }}/:id - Get by ID
  {{ entity.pluralName | lower }}.get("/:id", async (c) => {
    try {
      const id = c.req.param("id")
      const item = store.{{ entity.collectionName }}.get(id)
      if (!item) {
        return c.json({ error: { code: "not_found", message: "{{ entity.name }} not found" } }, 404)
      }
      return c.json(item)
    } catch (error: any) {
      return c.json({ error: { code: "query_error", message: error.message } }, 500)
    }
  })

  // POST {{ basePath }}/{{ entity.routePath }} - Create
  {{ entity.pluralName | lower }}.post("/", async (c) => {
    try {
      const body = await c.req.json()
      if (!body.id) {
        body.id = crypto.randomUUID()
      }
      const item = await store.{{ entity.collectionName }}.insertOne(body)
      return c.json(item, 201)
    } catch (error: any) {
      return c.json({ error: { code: "create_error", message: error.message } }, 500)
    }
  })

  // PATCH {{ basePath }}/{{ entity.routePath }}/:id - Update
  {{ entity.pluralName | lower }}.patch("/:id", async (c) => {
    try {
      const id = c.req.param("id")
      const item = store.{{ entity.collectionName }}.get(id)
      if (!item) {
        return c.json({ error: { code: "not_found", message: "{{ entity.name }} not found" } }, 404)
      }
      const body = await c.req.json()
      await store.{{ entity.collectionName }}.updateOne(id, body)
      return c.json({ ...item, ...body })
    } catch (error: any) {
      return c.json({ error: { code: "update_error", message: error.message } }, 500)
    }
  })

  // DELETE {{ basePath }}/{{ entity.routePath }}/:id - Delete
  {{ entity.pluralName | lower }}.delete("/:id", async (c) => {
    try {
      const id = c.req.param("id")
      const item = store.{{ entity.collectionName }}.get(id)
      if (!item) {
        return c.json({ error: { code: "not_found", message: "{{ entity.name }} not found" } }, 404)
      }
      await store.{{ entity.collectionName }}.deleteOne(id)
      return c.json({ success: true })
    } catch (error: any) {
      return c.json({ error: { code: "delete_error", message: error.message } }, 500)
    }
  })

  router.route("{{ basePath }}/{{ entity.routePath }}", {{ entity.pluralName | lower }})
{% endfor %}

  return router
}
`

/**
 * Convert entity name to plural form (simple rules)
 */
function pluralize(name: string): string {
  if (name.endsWith("y")) {
    return name.slice(0, -1) + "ies"
  }
  if (name.endsWith("s") || name.endsWith("x") || name.endsWith("ch") || name.endsWith("sh")) {
    return name + "es"
  }
  return name + "s"
}

/**
 * Convert entity name to collection name (camelCase + Collection)
 */
function toCollectionName(name: string): string {
  const camel = name.charAt(0).toLowerCase() + name.slice(1)
  return camel + "Collection"
}

/**
 * Convert entity name to route path (kebab-case, plural)
 */
function toRoutePath(name: string): string {
  // Convert PascalCase to kebab-case
  const kebab = name.replace(/([a-z])([A-Z])/g, "$1-$2").toLowerCase()
  return pluralize(kebab)
}

/**
 * Extract entity information from schema
 */
function extractEntities(schema: EnhancedJsonSchema, filter?: string[]): EntityInfo[] {
  const defs = schema.$defs || schema.definitions || {}
  const entities: EntityInfo[] = []

  for (const [name, def] of Object.entries(defs)) {
    // Skip if filter provided and entity not in filter
    if (filter && !filter.includes(name)) {
      continue
    }

    // Type assertion for definition object
    const typedDef = def as { type?: string; required?: string[]; properties?: Record<string, any> } | null

    // Skip non-object definitions
    if (!typedDef || typeof typedDef !== "object" || typedDef.type !== "object") {
      continue
    }

    const properties: PropertyInfo[] = []
    const required = new Set(typedDef.required || [])
    let hasReferences = false

    for (const [propName, propDef] of Object.entries(typedDef.properties || {})) {
      if (typeof propDef !== "object") continue

      const isReference = !!(propDef as any)["x-mst-type"]?.includes("reference")
      if (isReference) hasReferences = true

      properties.push({
        name: propName,
        type: (propDef as any).type || "string",
        required: required.has(propName),
        isReference,
        referenceTarget: (propDef as any)["x-reference-target"],
      })
    }

    entities.push({
      name,
      pluralName: pluralize(name),
      collectionName: toCollectionName(name),
      routePath: toRoutePath(name),
      properties,
      hasReferences,
    })
  }

  return entities
}

/**
 * Generate Hono CRUD routes from Enhanced JSON Schema
 *
 * @param config - Route generation configuration
 * @returns Generated TypeScript code and metadata
 *
 * @example
 * ```typescript
 * const result = createRoutes({
 *   schema: myEnhancedSchema,
 *   basePath: '/api',
 *   entities: ['User', 'Task']
 * })
 * console.log(result.code) // TypeScript route definitions
 * ```
 */
export function createRoutes(config: CreateRoutesConfig): CreateRoutesResult {
  const { schema, entities: entityFilter, basePath = "/api" } = config

  // Extract entity info from schema
  const entities = extractEntities(schema, entityFilter)

  if (entities.length === 0) {
    throw new Error("No entities found in schema to generate routes for")
  }

  // Get schema name from title or default
  const schemaName = schema.title || "Generated"

  // Configure Nunjucks for string templates
  const env = new nunjucks.Environment(null, { autoescape: false })

  // Add title filter
  env.addFilter("title", (str: string) => {
    return str
      .split(/[-_\s]/)
      .map((word) => word.charAt(0).toUpperCase() + word.slice(1))
      .join(" ")
  })

  // Render template
  const code = env.renderString(ROUTES_TEMPLATE, {
    schemaName,
    basePath,
    entities,
  })

  return {
    code,
    entities: entities.map((e) => e.name),
  }
}
