function generateDeleteFunction(model: PrismaModel, routePath: string): string[] {
  const name = model.name

  return [
    `/**`,
    ` * Delete a ${name}`,
    ` */`,
    `export async function delete${name}(args: { data: { id: string; userId?: string } }): Promise<{ success: boolean }> {`,
    `  const url = \`\${getApiBase()}/api/${routePath}/\${args.data.id}\``,
    `  const response = await fetch(url, {`,
    `    method: 'DELETE',`,
    `  })`,
    `  if (!response.ok) {`,
    `    const err = await response.json().catch(() => ({ error: { message: response.statusText } }))`,
    `    throw new Error(err.error?.message || 'Failed to delete ${name}')`,
    `  }`,
    `  return { success: true }`,
    `}`,
    '',
  ]
}
