/**
 * TypeScript Types Generator
 *
 * Generates TypeScript types from Prisma schema including:
 * - Model types
 * - Create/Update input types
 * - Hook types
 *
 * Supports both single-file and per-model output.
 */

import type { PrismaModel, PrismaField } from './prisma-generator'
import { getScalarFields, getRelationFields, toCamelCase } from './prisma-generator'

interface PrismaEnum {
  name: string
  values: { name: string }[]
}

// ============================================================================
// Types
// ============================================================================

export interface GeneratedTypeFile {
  /** Model name (e.g., 'Workspace') */
  modelName: string
  /** File name (e.g., 'workspace.types.ts') */
  fileName: string
  /** Generated code */
  code: string
}

// ============================================================================
// Utilities
// ============================================================================

/**
 * Convert model name to file name (kebab-case)
 */
function toFileName(name: string): string {
  return name.replace(/([a-z])([A-Z])/g, '$1-$2').toLowerCase()
}

/**
 * Generate TypeScript types for all models
 */
export function generateTypes(
  models: PrismaModel[], 
  enums: PrismaEnum[]
): string {
  const lines: string[] = [
    '/**',
    ' * Auto-generated Types',
    ' *',
    ' * TypeScript types for models and operations.',
    ' * Generated by `shogo generate` - DO NOT EDIT',
    ' */',
    '',
  ]

  // Generate enums
  if (enums.length > 0) {
    lines.push('// ============================================================================')
    lines.push('// Enums')
    lines.push('// ============================================================================')
    lines.push('')
    
    for (const enumDef of enums) {
      lines.push(`export type ${enumDef.name} = ${enumDef.values.map(v => `'${v.name}'`).join(' | ')}`)
      lines.push('')
    }
  }

  // Generate model types
  lines.push('// ============================================================================')
  lines.push('// Model Types')
  lines.push('// ============================================================================')
  lines.push('')

  for (const model of models) {
    lines.push(...generateModelType(model))
  }

  // Generate input types
  lines.push('// ============================================================================')
  lines.push('// Input Types')
  lines.push('// ============================================================================')
  lines.push('')

  for (const model of models) {
    lines.push(...generateInputTypes(model))
  }

  // Generate hook types
  lines.push(...generateHookTypes(models))

  return lines.join('\n')
}

/**
 * Map Prisma type to TypeScript type
 */
function mapPrismaType(field: PrismaField): string {
  // Handle relations
  if (field.kind === 'object') {
    return field.isList ? `${field.type}Type[]` : `${field.type}Type`
  }

  // Handle enums
  if (field.kind === 'enum') {
    return field.type
  }

  // Handle scalars
  switch (field.type) {
    case 'String':
      return 'string'
    case 'Int':
    case 'Float':
    case 'Decimal':
      return 'number'
    case 'Boolean':
      return 'boolean'
    case 'DateTime':
      return 'Date'
    case 'Json':
      return 'Record<string, unknown>'
    case 'BigInt':
      return 'bigint'
    case 'Bytes':
      return 'Buffer'
    default:
      return 'unknown'
  }
}

/**
 * Generate type for a single model
 */
function generateModelType(model: PrismaModel): string[] {
  const lines: string[] = [
    `export interface ${model.name}Type {`,
  ]

  // Add all fields
  for (const field of model.fields) {
    // Skip relation fields for now (keep types simple)
    if (field.kind === 'object') continue

    const tsType = mapPrismaType(field)
    const optional = !field.isRequired && !field.isId ? '?' : ''
    const nullable = !field.isRequired ? ' | null' : ''
    
    lines.push(`  ${field.name}${optional}: ${tsType}${!optional && nullable ? nullable : ''}`)
  }

  lines.push('}')
  lines.push('')

  return lines
}

/**
 * Generate create/update input types for a model
 */
function generateInputTypes(model: PrismaModel): string[] {
  const lines: string[] = []
  const scalarFields = getScalarFields(model)

  // Create input - required fields without defaults
  lines.push(`export interface ${model.name}CreateInput {`)
  for (const field of scalarFields) {
    // Skip auto-generated fields
    if (field.isId && field.hasDefaultValue) continue
    if (field.name === 'createdAt' || field.name === 'updatedAt') continue

    const tsType = mapPrismaType(field)
    const optional = !field.isRequired || field.hasDefaultValue ? '?' : ''
    
    lines.push(`  ${field.name}${optional}: ${tsType}`)
  }
  lines.push('}')
  lines.push('')

  // Update input - all fields optional
  lines.push(`export interface ${model.name}UpdateInput {`)
  for (const field of scalarFields) {
    // Skip ID and timestamp fields
    if (field.isId) continue
    if (field.name === 'createdAt' || field.name === 'updatedAt') continue

    const tsType = mapPrismaType(field)
    lines.push(`  ${field.name}?: ${tsType}`)
  }
  lines.push('}')
  lines.push('')

  return lines
}

/**
 * Generate hook types
 */
function generateHookTypes(models: PrismaModel[]): string[] {
  const lines: string[] = [
    '// ============================================================================',
    '// Hook Types',
    '// ============================================================================',
    '',
    'export interface HookContext {',
    '  userId?: string',
    '}',
    '',
    'export interface ListHookResult {',
    '  where?: Record<string, unknown>',
    '}',
    '',
    'export interface GetHookResult {',
    '  deny?: boolean',
    '}',
    '',
    'export interface UpdateHookResult<T> {',
    '  deny?: boolean',
    '  input?: T',
    '}',
    '',
    'export interface DeleteHookResult {',
    '  deny?: boolean',
    '}',
    '',
    'export interface ModelHooks<TCreate, TUpdate, TModel> {',
    '  beforeList?: (ctx: HookContext) => Promise<ListHookResult | void>',
    '  beforeGet?: (id: string, ctx: HookContext) => Promise<GetHookResult | void>',
    '  beforeCreate?: (input: TCreate, ctx: HookContext) => Promise<TCreate>',
    '  afterCreate?: (item: TModel, ctx: HookContext) => Promise<void>',
    '  beforeUpdate?: (id: string, input: TUpdate, ctx: HookContext) => Promise<UpdateHookResult<TUpdate> | void>',
    '  afterUpdate?: (item: TModel, ctx: HookContext) => Promise<void>',
    '  beforeDelete?: (id: string, ctx: HookContext) => Promise<DeleteHookResult | void>',
    '  afterDelete?: (id: string, ctx: HookContext) => Promise<void>',
    '}',
    '',
    'export interface ServerFunctionHooks {',
  ]

  for (const model of models) {
    lines.push(`  ${model.name}?: ModelHooks<${model.name}CreateInput, ${model.name}UpdateInput, ${model.name}Type>`)
  }

  lines.push('}')
  lines.push('')

  return lines
}

