/**
 * Generate types file for a single model
 */
export function generateModelTypes(model: PrismaModel, enums: PrismaEnum[] = [], fileExtension: 'ts' | 'tsx' = 'tsx'): GeneratedTypeFile {
  const modelName = model.name
  const fileName = `${toFileName(modelName)}.types.${fileExtension}`

  const lines: string[] = [
    '/**',
    ` * Auto-generated ${modelName} Types`,
    ' *',
    ' * Generated by @shogo-ai/sdk - DO NOT EDIT DIRECTLY',
    ' */',
    '',
  ]

  // Add relevant enums used by this model
  const modelEnumTypes = new Set(
    model.fields
      .filter(f => f.kind === 'enum')
      .map(f => f.type)
  )

  if (modelEnumTypes.size > 0) {
    const relevantEnums = enums.filter(e => modelEnumTypes.has(e.name))
    for (const enumDef of relevantEnums) {
      lines.push(`export type ${enumDef.name} = ${enumDef.values.map(v => `'${v.name}'`).join(' | ')}`)
      lines.push('')
    }
  }

  // Add model type
  lines.push(...generateModelType(model))

  // Add input types
  lines.push(...generateInputTypes(model))

  return {
    modelName,
    fileName,
    code: lines.join('\n'),
  }
}

/**
 * Generate types files for all models (per-model output)
 */
export function generateTypesPerModel(
  models: PrismaModel[],
  enums: PrismaEnum[] = [],
  fileExtension: 'ts' | 'tsx' = 'tsx'
): GeneratedTypeFile[] {
  return models.map(model => generateModelTypes(model, enums, fileExtension))
}

/**
 * Generate index file for types
 */
export function generateTypesIndex(models: PrismaModel[]): string {
  const lines: string[] = [
    '/**',
    ' * Auto-generated Types Index',
    ' *',
    ' * Generated by @shogo-ai/sdk - DO NOT EDIT DIRECTLY',
    ' */',
    '',
    '// Re-export all types',
  ]

  for (const model of models) {
    const fileName = toFileName(model.name)
    lines.push(`export * from "./${fileName}.types"`)
  }

  lines.push('')

  return lines.join('\n')
}
