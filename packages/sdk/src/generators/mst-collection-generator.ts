/**
 * MST Collection Generator
 *
 * Generates MobX-State-Tree collection models with:
 * - Items map (Map<string, Entity>)
 * - Loading/error state
 * - CRUD actions with optimistic updates
 * - Persistable, Queryable, Authorizable mixins
 */

import type { PrismaModel, PrismaField } from './prisma-generator'
import { toCamelCase, getIdField, getRelationFields } from './prisma-generator'

// ============================================================================
// Types
// ============================================================================

export interface GeneratedMSTCollectionFile {
  /** Model name (e.g., 'Workspace') */
  modelName: string
  /** File name (e.g., 'workspace.collection.ts') */
  fileName: string
  /** Generated code */
  code: string
}

// ============================================================================
// Utilities
// ============================================================================

/**
 * Convert model name to file name (kebab-case)
 */
function toFileName(name: string): string {
  return name.replace(/([a-z])([A-Z])/g, '$1-$2').toLowerCase()
}

/**
 * Convert model name to route path (kebab-case, plural)
 */
function toRoutePath(name: string): string {
  const kebab = name.replace(/([a-z])([A-Z])/g, '$1-$2').toLowerCase()
  if (kebab.endsWith('y')) return kebab.slice(0, -1) + 'ies'
  if (kebab.endsWith('s') || kebab.endsWith('x') || kebab.endsWith('ch') || kebab.endsWith('sh')) {
    return kebab + 'es'
  }
  return kebab + 's'
}

// ============================================================================
// MST Collection Generator
// ============================================================================

/**
 * Generate MST collection file for a single Prisma model
 */
export function generateMSTCollection(model: PrismaModel, fileExtension: 'ts' | 'tsx' = 'tsx'): GeneratedMSTCollectionFile {
  const modelName = model.name
  const modelLower = toCamelCase(modelName)
  const fileName = `${toFileName(modelName)}.collection.${fileExtension}`
  const routePath = toRoutePath(modelName)

  // Get relation field names for transformForMST
  const relationFields = getRelationFields(model)
  const relationFieldNames = relationFields.map(f => f.name)

  const lines: string[] = [
    '/**',
    ` * Auto-generated ${modelName} Collection`,
    ' *',
    ' * MST collection with CRUD operations, optimistic updates, and mixins.',
    ' * Generated by @shogo-ai/sdk - DO NOT EDIT DIRECTLY',
    ' */',
    '',
    'import { types, flow, getEnv, getSnapshot, applySnapshot, Instance } from "mobx-state-tree"',
    `import { ${modelName}Model, type I${modelName}, type I${modelName}SnapshotIn } from "./${toFileName(modelName)}.model"`,
    'import type { ISDKEnvironment } from "@shogo-ai/sdk"',
    '',
    '// ============================================================================',
    `// ${modelName} Collection`,
    '// ============================================================================',
    '',
    `const ENDPOINT = "/api/${routePath}"`,
    '',
    '/**',
    ` * ${modelName} Collection Model`,
    ' *',
    ' * Features:',
    ' * - Items stored in Map<id, Entity>',
    ' * - Loading/error state tracking',
    ' * - CRUD with optimistic updates and rollback',
    ' * - Persistence via SDK HttpClient',
    ' */',
    `export const ${modelName}Collection = types`,
    `  .model("${modelName}Collection", {`,
    `    items: types.map(${modelName}Model),`,
    '    isLoading: types.optional(types.boolean, false),',
    '    error: types.maybeNull(types.string),',
    '  })',
    '',
    '  // =========================================================================',
    '  // Views',
    '  // =========================================================================',
    '  .views(self => ({',
    '    /** Get all items as array */',
    `    get all(): I${modelName}[] {`,
    '      return Array.from(self.items.values())',
    '    },',
    '',
    '    /** Get item by ID */',
    `    get(id: string): I${modelName} | undefined {`,
    '      return self.items.get(id)',
    '    },',
    '',
    '    /** Get item count */',
    '    get count(): number {',
    '      return self.items.size',
    '    },',
    '',
    '    /** Model name for persistence context */',
    '    get modelName(): string {',
    `      return "${modelName}"`,
    '    },',
    '',
    '    /** Filter items by predicate */',
    `    filter(predicate: (item: I${modelName}) => boolean): I${modelName}[] {`,
    '      return this.all.filter(predicate)',
    '    },',
    '',
    '    /** Find first item matching predicate */',
    `    find(predicate: (item: I${modelName}) => boolean): I${modelName} | undefined {`,
    '      return this.all.find(predicate)',
    '    },',
    '  }))',
    '',
    '  // =========================================================================',
    '  // Actions',
    '  // =========================================================================',
    '  .actions(self => {',
    '    // Track pending operations for optimistic updates',
    '    const pendingCreates = new Map<string, any>()',
    '    const pendingUpdates = new Map<string, any>()',
    '    const pendingDeletes = new Set<string>()',
    '',
    '    return {',
    '      /** Set loading state */',
    '      setLoading(loading: boolean) {',
    '        self.isLoading = loading',
    '      },',
    '',
    '      /** Set error state */',
    '      setError(error: string | null) {',
    '        self.error = error',
    '      },',
    '',
    '      /** Clear error state */',
    '      clearError() {',
    '        self.error = null',
    '      },',
    '',
    '      /** Add an item directly (for external updates like WebSocket) */',
    `      addItem(item: I${modelName}SnapshotIn) {`,
    '        self.items.put(item)',
    '      },',
    '',
    '      /** Remove an item directly */',
    '      removeItem(id: string) {',
    '        self.items.delete(id)',
    '      },',
    '',
    '      /** Check if item has pending operation */',
    '      isPending(id: string): boolean {',
    '        return pendingCreates.has(id) || pendingUpdates.has(id) || pendingDeletes.has(id)',
    '      },',
    '',
    '      // =====================================================================',
    '      // CRUD Operations with Optimistic Updates',
    '      // =====================================================================',
    '',
    '      /**',
    '       * Load all items from API',
    '       * Uses merge strategy to preserve existing MST node references',
    '       */',
    '      loadAll: flow(function* (filter?: Record<string, any>) {',
    '        self.isLoading = true',
    '        self.error = null',
    '',
    '        try {',
    '          const env = getEnv<ISDKEnvironment>(self)',
    '          const params = filter ? new URLSearchParams(filter as any).toString() : ""',
    '          const url = params ? `${ENDPOINT}?${params}` : ENDPOINT',
    '',
    '          const response = yield env.http.get<{ ok: boolean; items?: any[] }>(url)',
    '',
    '          if (response.data?.ok && response.data.items) {',
    '            // Use merge strategy to preserve existing node references',
    '            const newIds = new Set<string>()',
    '',
    '            for (const item of response.data.items) {',
    '              const transformed = transformForMST(item)',
    '              const id = transformed.id',
    '              newIds.add(id)',
    '',
    '              const existing = self.items.get(id)',
    '              if (existing) {',
    '                // Update existing item in place (preserves node reference)',
    '                applySnapshot(existing, transformed)',
    '              } else {',
    '                // Add new item',
    '                self.items.put(transformed)',
    '              }',
    '            }',
    '',
    '            // Remove items that are no longer in the response',
    '            for (const id of self.items.keys()) {',
    '              if (!newIds.has(id)) {',
    '                self.items.delete(id)',
    '              }',
    '            }',
    '          }',
    '',
    '          self.isLoading = false',
    '          return self.all',
    '        } catch (error: any) {',
    '          self.error = error.message || "Failed to load"',
    '          self.isLoading = false',
    '          throw error',
    '        }',
    '      }),',
    '',
    '      /**',
    '       * Load single item by ID',
    '       */',
    '      loadById: flow(function* (id: string) {',
    '        try {',
    '          const env = getEnv<ISDKEnvironment>(self)',
    '          const response = yield env.http.get<{ ok: boolean; data?: any }>(`${ENDPOINT}/${id}`)',
    '',
    '          if (response.data?.ok && response.data.data) {',
    '            const transformed = transformForMST(response.data.data)',
    '            self.items.put(transformed)',
    '            return self.items.get(id)',
    '          }',
    '',
    '          return undefined',
    '        } catch (error: any) {',
    '          self.error = error.message || "Failed to load"',
    '          throw error',
    '        }',
    '      }),',
    '',
    '      /**',
    '       * Create new item with optimistic update',
    '       */',
    `      create: flow(function* (input: Partial<I${modelName}SnapshotIn>) {`,
    '        const tempId = `temp-${crypto.randomUUID()}`',
    '        const now = Date.now()',
    '',
    '        // Create optimistic item',
    '        const optimistic = {',
    '          id: tempId,',
    '          ...input,',
    '          createdAt: now,',
    '          updatedAt: now,',
    '        }',
    '',
    '        // Add optimistically',
    '        pendingCreates.set(tempId, optimistic)',
    '        self.items.put(optimistic as any)',
    '        self.error = null',
    '',
    '        try {',
    '          const env = getEnv<ISDKEnvironment>(self)',
    '          const response = yield env.http.post<{ ok: boolean; data?: any }>(ENDPOINT, input)',
    '',
    '          if (!response.data?.ok || !response.data.data) {',
    '            throw new Error("Create failed")',
    '          }',
    '',
    '          // Replace temp with real item',
    '          const realItem = transformForMST(response.data.data)',
    '          pendingCreates.delete(tempId)',
    '          self.items.delete(tempId)',
    '          self.items.put(realItem)',
    '',
    '          return self.items.get(realItem.id)',
    '        } catch (error: any) {',
    '          // Rollback',
    '          pendingCreates.delete(tempId)',
    '          self.items.delete(tempId)',
    '          self.error = error.message || "Create failed"',
    '          throw error',
    '        }',
    '      }),',
    '',
    '      /**',
    '       * Update item with optimistic update',
    '       */',
    `      update: flow(function* (id: string, changes: Partial<I${modelName}SnapshotIn>) {`,
    '        const existing = self.items.get(id)',
    '        if (!existing) {',
    '          throw new Error("Item not found")',
    '        }',
    '',
    '        // Don\'t allow concurrent updates',
    '        if (pendingUpdates.has(id)) {',
    '          throw new Error("Update already in progress")',
    '        }',
    '',
    '        // Save previous state for rollback',
    '        const previousState = getSnapshot(existing)',
    '        pendingUpdates.set(id, previousState)',
    '',
    '        // Apply optimistically',
    '        existing.update({ ...changes, updatedAt: Date.now() })',
    '        self.error = null',
    '',
    '        try {',
    '          const env = getEnv<ISDKEnvironment>(self)',
    '          const response = yield env.http.patch<{ ok: boolean; data?: any }>(`${ENDPOINT}/${id}`, changes)',
    '',
    '          if (!response.data?.ok || !response.data.data) {',
    '            throw new Error("Update failed")',
    '          }',
    '',
    '          // Apply server response',
    '          const serverData = transformForMST(response.data.data)',
    '          pendingUpdates.delete(id)',
    '          applySnapshot(existing, serverData)',
    '',
    '          return existing',
    '        } catch (error: any) {',
    '          // Rollback',
    '          const prev = pendingUpdates.get(id)',
    '          pendingUpdates.delete(id)',
    '          if (prev) {',
    '            applySnapshot(existing, prev)',
    '          }',
    '          self.error = error.message || "Update failed"',
    '          throw error',
    '        }',
    '      }),',
    '',
    '      /**',
    '       * Delete item with optimistic update',
    '       */',
    '      delete: flow(function* (id: string) {',
    '        const existing = self.items.get(id)',
    '        if (!existing) return',
    '',
    '        // Don\'t allow concurrent deletes',
    '        if (pendingDeletes.has(id)) return',
    '',
    '        // Save for rollback',
    '        const previousState = getSnapshot(existing)',
    '        pendingDeletes.add(id)',
    '',
    '        // Remove optimistically',
    '        self.items.delete(id)',
    '        self.error = null',
    '',
    '        try {',
    '          const env = getEnv<ISDKEnvironment>(self)',
    '          yield env.http.delete(`${ENDPOINT}/${id}`)',
    '',
    '          pendingDeletes.delete(id)',
    '        } catch (error: any) {',
    '          // Rollback',
    '          pendingDeletes.delete(id)',
    '          self.items.put(previousState as any)',
    '          self.error = error.message || "Delete failed"',
    '          throw error',
    '        }',
    '      }),',
    '',
    '      /** Clear all items */',
    '      clear() {',
    '        self.items.clear()',
    '        self.error = null',
    '        self.isLoading = false',
    '      },',
    '    }',
    '  })',
    '',
    '// ============================================================================',
    '// Helpers',
    '// ============================================================================',
    '',
    `// Relation fields that expect IDs (safeReference)`,
    `const relationFields = ${JSON.stringify(relationFieldNames)}`,
    '',
    '/**',
    ' * Transform API response for MST compatibility:',
    ' * - Convert ISO date strings to timestamps',
    ' * - Extract IDs from nested relation objects (for safeReference)',
    ' * - Handle null -> undefined for optional fields',
    ' */',
    'function transformForMST(obj: any): any {',
    '  if (!obj || typeof obj !== "object") return obj',
    '',
    '  const dateFields = ["createdAt", "updatedAt", "expiresAt", "publishedAt", "readAt", "emailSentAt", "lastActiveAt"]',
    '  const result: Record<string, any> = {}',
    '',
    '  for (const [key, value] of Object.entries(obj)) {',
    '    // Skip null values (MST uses undefined)',
    '    if (value === null) continue',
    '',
    '    // Convert date strings to timestamps',
    '    if (dateFields.includes(key) && typeof value === "string") {',
    '      result[key] = new Date(value).getTime()',
    '    }',
    '    // For relation fields, extract only the ID (for safeReference)',
    '    else if (relationFields.includes(key) && value && typeof value === "object" && !Array.isArray(value) && (value as any).id) {',
    '      result[key] = (value as any).id',
    '    }',
    '    // For array relation fields, extract IDs',
    '    else if (relationFields.includes(key) && Array.isArray(value)) {',
    '      result[key] = value.map((item: any) => (item && typeof item === "object" && item.id) ? item.id : item)',
    '    }',
    '    // Skip nested objects that are not relations (they might be JSON fields)',
    '    else if (value && typeof value === "object" && !Array.isArray(value)) {',
    '      // Only include if it has an id (likely a reference) or is a plain data object',
    '      if ((value as any).id && !relationFields.includes(key)) {',
    '        result[key] = value // Keep as-is for JSON fields',
    '      } else if (!(value as any).id) {',
    '        result[key] = value // Keep plain objects (like metadata)',
    '      }',
    '      // If it has an id but is a relation field, we already handled it above',
    '    }',
    '    else {',
    '      result[key] = value',
    '    }',
    '  }',
    '',
    '  return result',
    '}',
    '',
    '// Type exports',
    `export type I${modelName}Collection = Instance<typeof ${modelName}Collection>`,
    '',
  ]

  return {
    modelName,
    fileName,
    code: lines.join('\n'),
  }
}

/**
 * Generate MST collections for all Prisma models
 */
export function generateMSTCollections(models: PrismaModel[], fileExtension: 'ts' | 'tsx' = 'tsx'): GeneratedMSTCollectionFile[] {
  return models
    .filter(model => getIdField(model))
    .map(model => generateMSTCollection(model, fileExtension))
}
