/**
 * Hono Routes Generator (Per-Model)
 *
 * Generates one route file per Prisma model with CRUD operations.
 * Adapted from @shogo/state-api for SDK consolidation.
 */

import type { PrismaModel, PrismaField } from './prisma-generator'
import { toCamelCase, getIdField } from './prisma-generator'

// ============================================================================
// Types
// ============================================================================

export interface RouteGeneratorConfig {
  /** Base path for routes (default: '/api') */
  basePath?: string
  /** File extension: 'ts' or 'tsx' (default: 'tsx') */
  fileExtension?: 'ts' | 'tsx'
}

export interface GeneratedRouteFile {
  /** Model name (e.g., 'Workspace') */
  modelName: string
  /** File name (e.g., 'workspace.routes.ts') */
  fileName: string
  /** Generated code */
  code: string
}

export interface GeneratedHooksFile {
  /** Model name (e.g., 'Workspace') */
  modelName: string
  /** File name (e.g., 'workspace.hooks.ts') */
  fileName: string
  /** Generated code */
  code: string
}

// ============================================================================
// Utilities
// ============================================================================

/**
 * Convert model name to route path (kebab-case, plural)
 */
function toRoutePath(name: string): string {
  const kebab = name.replace(/([a-z])([A-Z])/g, '$1-$2').toLowerCase()
  // Simple pluralization
  if (kebab.endsWith('y')) return kebab.slice(0, -1) + 'ies'
  if (kebab.endsWith('s') || kebab.endsWith('x') || kebab.endsWith('ch') || kebab.endsWith('sh')) {
    return kebab + 'es'
  }
  return kebab + 's'
}

/**
 * Convert model name to file name (kebab-case)
 */
function toFileName(name: string): string {
  return name.replace(/([a-z])([A-Z])/g, '$1-$2').toLowerCase()
}

