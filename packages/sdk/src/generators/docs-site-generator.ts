// ============================================================================
// tsconfig.json for the docs site
// ============================================================================

export function generateDocsTsConfig(): GeneratedDocFile {
  const content = {
    extends: '@docusaurus/tsconfig',
    compilerOptions: {
      baseUrl: '.',
    },
  }

  return {
    path: 'tsconfig.json',
    content: JSON.stringify(content, null, 2) + '\n',
    skipIfExists: true,
  }
}
