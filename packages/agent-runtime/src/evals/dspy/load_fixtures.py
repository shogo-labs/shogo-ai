"""Shared loader for JSON fixture files.

All dataset files use this to load from the single source of truth:
  packages/agent-runtime/src/evals/fixtures/*.json

The fixtures are generated by the TypeScript extractor:
  bun run packages/agent-runtime/src/evals/extract-fixtures.ts
"""

import json
from pathlib import Path

FIXTURES_DIR = Path(__file__).resolve().parent.parent / "fixtures"


def load_fixture(track_name: str) -> dict:
    """Load a JSON fixture file by track name."""
    path = FIXTURES_DIR / f"{track_name}.json"
    if not path.exists():
        raise FileNotFoundError(
            f"Fixture not found: {path}\n"
            f"Run: bun run packages/agent-runtime/src/evals/extract-fixtures.ts"
        )
    with open(path) as f:
        return json.load(f)


def get_constants(fixture: dict) -> dict:
    """Extract the constants block from a fixture."""
    return fixture.get("constants", {})


def get_examples(fixture: dict) -> list[dict]:
    """Extract the examples list from a fixture."""
    return fixture.get("examples", [])
