/**
 * Reads the shell-integration script files under
 *   apps/desktop/src/pty-host/shell-integration/scripts/
 * and writes their contents as TypeScript string constants in
 *   apps/desktop/src/pty-host/shell-integration/embedded-scripts.generated.ts
 *
 * This lets us:
 *   - keep the .sh / .zsh / .fish / .ps1 files on disk with proper
 *     syntax highlighting in the editor,
 *   - load them from `index.ts` without any FS access at runtime,
 *   - ship the bundled pty-host.js as a single file (no scripts/
 *     subdirectory to copy alongside).
 *
 * Run by `npm run bundle:pty-host` (see bundle-pty-host.mjs) and also
 * once at commit time so the generated file is checked in for tests.
 */
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const ROOT = path.resolve(__dirname, '..', 'src', 'pty-host', 'shell-integration')
const SCRIPTS = path.join(ROOT, 'scripts')
const OUT = path.join(ROOT, 'embedded-scripts.generated.ts')

const FILES = [
  ['BASH',         'bash.sh'],
  ['ZSH_ENV',      'zsh/.zshenv'],
  ['ZSH_PROFILE',  'zsh/.zprofile'],
  ['ZSH_RC',       'zsh/.zshrc'],
  ['ZSH_LOGIN',    'zsh/.zlogin'],
  ['ZSH_LOGOUT',   'zsh/.zlogout'],
  ['FISH',         'fish.fish'],
  ['PWSH',         'pwsh.ps1'],
  ['NUSHELL',      'nu.nu'],
]

function escapeForBacktick(s) {
  return s.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${')
}

const banner = `// SPDX-License-Identifier: MIT
// Copyright (C) 2026 Shogo Technologies, Inc.
//
// AUTO-GENERATED by scripts/generate-shell-integration-embeds.mjs.
// DO NOT EDIT BY HAND. Edit the .sh / .zsh / .fish / .ps1 source files
// under scripts/ and re-run \`node scripts/generate-shell-integration-embeds.mjs\`.

`

let body = ''
for (const [name, rel] of FILES) {
  const text = readFileSync(path.join(SCRIPTS, rel), 'utf8')
  body += `export const ${name} = \`${escapeForBacktick(text)}\`\n\n`
}

mkdirSync(ROOT, { recursive: true })
writeFileSync(OUT, banner + body, 'utf8')

console.log(`[generate-shell-integration-embeds] wrote ${path.relative(process.cwd(), OUT)}`)
